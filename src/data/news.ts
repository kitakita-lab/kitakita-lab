/**
 * News データ。
 *
 * 将来的なCMS化を想定したデータ構造です。
 * `body` は段落の配列で、各お知らせの詳細ページに表示されます。
 * CMS導入時は、この配列を API レスポンス（同じ型）に置き換えるだけで移行できます。
 */

export type NewsCategory = 'お知らせ' | 'プレスリリース' | 'イベント' | 'メディア'

export type NewsItem = {
  /** URL スラッグ（/news/:slug） */
  slug: string
  title: string
  /** ISO 形式の日付（YYYY-MM-DD）。並び順の基準に使う。 */
  date: string
  /**
   * 日付の代わりに表示するラベル。正式な公開日が未確定のお知らせは
   * '公開準備中' などを設定し、確定していない日付を公開面に出さない。
   */
  dateLabel?: string
  category: NewsCategory
  excerpt: string
  /** 詳細本文（段落の配列） */
  body: string[]
}

